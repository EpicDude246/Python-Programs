import turtle
t = turtle.Turtle()
t.speed(0)
t.goto(0,0)
def f(t):
    t.right(90)
    t.forward(200)
    t.up()
    t.left(180)
    t.forward(200)
    t.right(90)
    t.down()
    t.forward(100)
    t.up()
    t.left(180)
    t.forward(100)
    t.right(270)
    t.forward(80)
    t.left(90)
    t.down()
    t.forward(100)
f(t)
    
    
